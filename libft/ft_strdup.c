#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*new;
	int		size;
	int		i;

	size = ft_strlen((char *)s1);
	new = malloc((size + 1) * sizeof(char));
	if (new == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
